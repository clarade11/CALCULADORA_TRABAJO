package calculadora;

public class Llamado {
	
	/**
	 * 
	 * @param a
	 * @param b
	 * @return r
	 */
	public double suma(double a, double b) {
		double r = a + b;
		return r;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return r
	 */
	public double resta(double a, double b) {
		double r = a - b;
		return r;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return r
	 */
	public double mul(double a, double b) {
		double r = a * b;
		return r;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return r
	 */
	public double div(double a, double b) {
		double r = a / b;
		return r;
	}
}
